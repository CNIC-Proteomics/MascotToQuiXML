
public class Varmods {
	
	private char assignedChar;
	private double deltaMass;
	private String name;
	private int index;

	/**
	 * @return the index
	 */
	public int getIndex() {
		return index;
	}
	/**
	 * @param index the index to set
	 */
	public void setIndex(int index) {
		this.index = index;
	}

	/**
	 * @return the assignedChar
	 */
	public char getAssignedChar() {
		return assignedChar;
	}
	/**
	 * @param assignedChar the assignedChar to set
	 */
	public void setAssignedChar(char assignedChar) {
		this.assignedChar = assignedChar;
	}
	/**
	 * @return the deltaMass
	 */
	public double getDeltaMass() {
		return deltaMass;
	}
	/**
	 * @param deltaMass the deltaMass to set
	 */
	public void setDeltaMass(double deltaMass) {
		this.deltaMass = deltaMass;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
	

}
